#include "libft.h"

char	*ft_strdup(char const *src)
{
	size_t	n;
	char	*dst;
	
	n = ft_strlen(src);
	dst = (char*)malloc(sizeof(const char) * (n + 1));
	if (dst)
		ft_memcpy(dst, src, n + 1);
	return (dst);
}
