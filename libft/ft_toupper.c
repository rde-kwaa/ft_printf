#include "libft.h"

char	*ft_toupper(char *s)
{
	int		i;

	i = 0;
	while (s[i++])
		if (s[i - 1] >= 'a' && s[i - 1] <= 'z')
			s[i - 1] -= 32;
	return (s);
}
